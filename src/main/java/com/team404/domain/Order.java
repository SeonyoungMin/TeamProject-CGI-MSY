	private String tradeType;

	// 직거래 전용
	private String meetingPlace;
	@JsonFormat(shape = JsonFormat.Shape.STRING, pattern = "yyyy-MM-dd HH:mm")
	private Timestamp meetingTime;
	private String buyerMessage;

